/** Search NOC outcomes by code, name, domain, category, and indicators */
export function searchNocOutcomes(query: string): NocOutcome[] {
  if (!query || query.trim().length === 0) return [];
  const q = query.trim().toLowerCase();
  return NOC_OUTCOMES.filter((o) => {
    // Match by code (prefix or anywhere)
    if (o.code.toLowerCase().startsWith(q)) return true;
    if (o.code.includes(q)) return true;
    // Match by name
    if (o.name.toLowerCase().includes(q)) return true;
    // Match by domain
    if (o.domain.toLowerCase().includes(q)) return true;
    // Match by category
    if (o.category.includes(q)) return true;
    // Match by class name
    if (o.className.toLowerCase().includes(q)) return true;
    // Match by indicators
    if (o.indicators.some((ind) => ind.toLowerCase().includes(q))) return true;
    return false;
  }).slice(0, 20); // Limit results
}
